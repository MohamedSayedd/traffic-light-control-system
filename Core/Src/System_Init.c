void MX_GPIO_Init(void) {
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    __HAL_RCC_GPIOA_CLK_ENABLE();
    __HAL_RCC_GPIOB_CLK_ENABLE();

    // Configure GPIO pins for traffic lights
    GPIO_InitStruct.Pin = NS_GREEN_Pin | NS_YELLOW_Pin | NS_RED_Pin |
                          EW_GREEN_Pin | EW_YELLOW_Pin | EW_RED_Pin;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

    // Configure GPIO pins for pedestrian signals
    GPIO_InitStruct.Pin = NS_WALK_Pin | NS_DONT_WALK_Pin |
                          EW_WALK_Pin | EW_DONT_WALK_Pin;
    HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

    // Configure GPIO pins for pedestrian buttons as external interrupts
    GPIO_InitStruct.Pin = GPIO_PIN_6 | GPIO_PIN_7;
    GPIO_InitStruct.Mode = GPIO_MODE_IT_FALLING;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

    // Enable and set EXTI line 9-5 Interrupt to the lowest priority
    HAL_NVIC_SetPriority(EXTI9_5_IRQn, 2, 0);
    HAL_NVIC_EnableIRQ(EXTI9_5_IRQn);
}

void System_Init(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();

}

